package variable;

public class Var8 {

    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i; // -2,147,483,648 ~ 2,147,483,647
        long l; // -9,223,372,036,854,775,808L ~ 9,223,372,036,854,775,807L
        // -> int, long 사용

        // 실수
        float f = 0.0f;
        double d = 0.1232;
        // double 사용
    }
}
