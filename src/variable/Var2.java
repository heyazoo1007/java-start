package variable;

public class Var2 {

    // 변수는 변하는 데이터를 담는 그릇
    // 변수를 사용하면 중복되는 값을 반복해서 입력하지 않아도 된다.
    public static void main(String[] args) {
        int a; // 변수 선언
        a = 20; // 10 -> 20 변수 초기화

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
    }
}
